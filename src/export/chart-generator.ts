// ─── Internal Helpers ─────────────────────────────────────────────────────────

function buildCategoryChartSection(result: ComparisonResult): string {
  if (result.vendors.length === 0) return '';

  const catId = result.categoryName.replace(/[^a-zA-Z0-9]/g, '_');
  const barData = buildBarChartData(result.scoreDistribution);
  const pieData = buildPieChartData(marketPositionDistribution(result.vendors));
  const criteriaComparisonData = buildCriteriaComparisonChartData(result.vendors);

  // Top vendor radar
  const topVendor = result.vendors[0];
  const radarData = topVendor ? buildRadarChartData(criteriaRadarData(topVendor), topVendor.name) : '';

  return `
  <h2>${escHtml(result.categoryName)}</h2>
  <div class="stat-row">
    <div class="stat">Vendors Analysed <span>${result.vendors.length}</span></div>
    <div class="stat">Top Vendor <span>${escHtml(result.topVendor)}</span></div>
    <div class="stat">Average Score <span>${result.averageScore}/10</span></div>
  </div>
  <div class="grid">
    <div class="card">
      <h3>Score Ranking</h3>
      <div class="chart-wrapper"><canvas id="bar_${catId}"></canvas></div>
    </div>
    <div class="card">
      <h3>Market Position Distribution</h3>
      <div class="chart-wrapper"><canvas id="pie_${catId}"></canvas></div>
    </div>
    <div class="card">
      <h3>Criteria Comparison by Vendor</h3>
      <div class="chart-wrapper"><canvas id="criteria_${catId}"></canvas></div>
    </div>
    ${topVendor ? `
    <div class="card">
      <h3>Top Vendor Criteria — ${escHtml(topVendor.name)}</h3>
      <div class="chart-wrapper"><canvas id="radar_${catId}"></canvas></div>
    </div>` : ''}
  </div>

  <script>
  (function() {
    // Bar chart
    new Chart(document.getElementById('bar_${catId}'), {
      type: 'bar',
      data: ${barData},
      options: {
        indexAxis: 'y',
        responsive: true,
        maintainAspectRatio: false,
        plugins: { legend: { display: false } },
        scales: {
          x: { min: 0, max: 10, ticks: { stepSize: 1 } }
        }
      }
    });

    // Pie chart
    new Chart(document.getElementById('pie_${catId}'), {
      type: 'doughnut',
      data: ${pieData},
      options: {
        responsive: true,
        maintainAspectRatio: false,
        plugins: { legend: { position: 'right' } }
      }
    });

    // Criteria comparison chart
    new Chart(document.getElementById('criteria_${catId}'), {
      type: 'bar',
      data: ${criteriaComparisonData},
      options: {
        responsive: true,
        maintainAspectRatio: false,
        plugins: {
          legend: { position: 'bottom' },
        },
        scales: {
          y: { min: 0, max: 10, ticks: { stepSize: 1 } },
        },
      }
    });

    ${topVendor && radarData ? `
    // Radar chart
    new Chart(document.getElementById('radar_${catId}'), {
      type: 'radar',
      data: ${radarData},
      options: {
        responsive: true,
        maintainAspectRatio: false,
        scales: { r: { min: 0, max: 10, ticks: { stepSize: 2 } } }
      }
    });` : ''}
  })();
  </script>`;
}

const CHART_COLORS = [
  '#38bdf8', '#818cf8', '#34d399', '#fb923c', '#f472b6',
  '#a78bfa', '#4ade80', '#facc15', '#60a5fa', '#f87171',
];

function buildBarChartData(data: ChartDataPoint[]): string {
  return JSON.stringify({
    labels: data.map(d => d.label),
    datasets: [{
      label: 'Score',
      data: data.map(d => d.value),
      backgroundColor: data.map((d, i) => d.color ?? CHART_COLORS[i % CHART_COLORS.length]),
      borderRadius: 4,
    }],
  });
}

function buildPieChartData(data: ChartDataPoint[]): string {
  return JSON.stringify({
    labels: data.map(d => d.label),
    datasets: [{
      data: data.map(d => d.value),
      backgroundColor: CHART_COLORS.slice(0, data.length),
    }],
  });
}

function buildRadarChartData(data: ChartDataPoint[], vendorName: string): string {
  return JSON.stringify({
    labels: data.map(d => d.label),
    datasets: [{
      label: vendorName,
      data: data.map(d => d.value),
      fill: true,
      backgroundColor: 'rgba(56, 189, 248, 0.15)',
      borderColor: '#38bdf8',
      pointBackgroundColor: '#38bdf8',
    }],
  });
}

function buildCriteriaComparisonChartData(vendors: VendorWithCriteria[]): string {
  const ranked = [...vendors].sort((a, b) => b.rankingScore - a.rankingScore);
  const labels = ranked.map(v => v.name);
  const metadata = getRankingCriteriaMetadataForCategory(ranked[0]?.categoryName);

  const datasets = metadata.map((criterion, i) => ({
    label: criterion.label,
    data: ranked.map(v => (v.criteria ? (v.criteria as any)[criterion.key] ?? 0 : 0)),
    backgroundColor: CHART_COLORS[i % CHART_COLORS.length],
    borderRadius: 3,
  }));

  return JSON.stringify({ labels, datasets });
}

function escHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
