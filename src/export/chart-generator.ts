// ─── ASCII Terminal Charts ───────────────────────────────────────────────────

/**
 * Prints an ASCII bar chart to console for a category's rankings.
 */
export function printAsciiRanking(result: ComparisonResult): void {
  console.log(`\n  Vendor Rankings — ${result.categoryName}`);
  console.log('  ' + '─'.repeat(70));
  if (result.scoreDistribution.length === 0) {
    console.log('  (no vendors)');
    return;
  }
  const chart = asciiBarChart(result.scoreDistribution, 40);
  chart.split('\n').forEach(line => console.log('  ' + line));
  console.log('  ' + '─'.repeat(70));
  console.log(`  Average: ${result.averageScore}/10 | Top: ${result.topVendor}`);
  console.log();
}

